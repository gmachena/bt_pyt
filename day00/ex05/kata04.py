t = (0, 4, 132.42222, 10000, 12345.67)
x = list(t)
i = 2

def sci(n):
    a = '%E' % n
    return a.split('E')[0].rstrip('0').rstrip('.') + 'E' + a.split('E')[1]

while (i < len(t)):
    x[i] = sci(t[i])
    i += 1

t = tuple(x)

print("day_0{}, ex_0{}: {}, {}, {}".format(*t))